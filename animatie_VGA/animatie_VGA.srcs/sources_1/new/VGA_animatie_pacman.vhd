    process(clk)
    begin
    if clk='1' and clk'event then
    if counter<=49999999 then
       counter<=counter+1;
    else
       counter<=0;
       clock_nou<=not(clock_nou);
       stare<=stare+1; 
    end if;
    end if;
    end process;
    
    ---------------imagine in functie de stare--------------------------------
    process(clock_nou)
